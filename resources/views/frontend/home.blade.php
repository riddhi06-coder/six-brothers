        <script>
            $(document).ready(function () {
              const referrer = document.referrer;
            
              // Show modal only if coming from an external page or no referrer (e.g., direct visit)
              const isFromExternalOrDirect = !referrer || !referrer.includes(window.location.hostname);
            
              // Only check time and show modal on index.html
              const isIndexPage = window.location.pathname.endsWith("index.html") || window.location.pathname === "/";
            
              if (isFromExternalOrDirect && isIndexPage) {
                $.ajax({
                  url: "get_submit_time.php",
                  type: "GET",
                  dataType: "json",
                  success: function (response) {
                    if (response.status === "success" && response.timestamp) {
                      const now = Date.now(); // current time in ms
                      const lastSubmitTime = response.timestamp * 1000; // convert to ms
                      const timePassed = now - lastSubmitTime;
            
                      if (timePassed >= 3600000) {
                        // 1 hour has passed since last submit
                        $("#popupModal").modal("show");
                      }
                    } else {
                      // No previous submission, show modal
                      $("#popupModal").modal("show");
                    }
                  },
                  error: function () {
                    console.error("Could not check submission time.");
                  }
                });
              }
            
              // Form validation and submission
              $("#popupForm").submit(function (event) {
                event.preventDefault();
                let isValid = true;
                $(".error-message").text("");
            
                const firstName = $("#first-name").val().trim();
                const lastName = $("#last-name").val().trim();
                const email = $("#email_id").val().trim();
                const country = $("#country").val().trim();
                const dob = $("#dob").val().trim();
                const subscribe = $("#subscribe").is(":checked");
            
                const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            
                if (firstName === "") {
                  $("#error-first-name").text("First name is required.");
                  isValid = false;
                }
                if (lastName === "") {
                  $("#error-last-name").text("Last name is required.");
                  isValid = false;
                }
                if (email === "") {
                  $("#error-email").text("Email is required.");
                  isValid = false;
                } else if (!emailPattern.test(email)) {
                  $("#error-email").text("Enter a valid email address.");
                  isValid = false;
                }
                if (country === "") {
                  $("#error-country").text("Country is required.");
                  isValid = false;
                }
                if (dob === "") {
                  $("#error-dob").text("Date of Birth is required.");
                  isValid = false;
                }
                if (!subscribe) {
                  $("#error-subscribe").text("You must consent to subscribe.");
                  isValid = false;
                }

                if (!isValid) return;
            
                $.ajax({
                  type: "POST",
                  url: "send_mail.php",
                  data: $("#popupForm").serialize(),
                  dataType: "json",
                  success: function (response) {
                    if (response.status === "success") {
                      toastr.success(response.message);
                      $("#popupForm")[0].reset();
            
                      setTimeout(() => {
                        $("#popupModal").modal("hide");
                        window.location.href = "https://sixbrothers.com/thank-you";
                      }, 1500);
                    } else {
                      toastr.error(response.message);
                    }
                  },
                  error: function () {
                    toastr.error("Something went wrong. Please try again.");
                  },
                });
              });
            });
        </script>
